function waitForElement(cssSelector, callback) {
    var stop,
        elementCached,
        timeout,
        check = function () {
            try {
                elementCached = document.querySelector(cssSelector)

                if (stop) return

                if (elementCached) {
                    callback(elementCached)
                    clearTimeout(timeout)
                } else {
                    window.requestAnimationFrame(check)
                }
            } catch (err) {
                console.log(err)
            }
        }

    window.requestAnimationFrame(check)

    timeout = setTimeout(function () {
        stop = true
    }, 5000)
}

const testNumber = '8004'
const testType = 'v1'
const testClass = `spz_${testNumber}_${testType}`

waitForElement('.hero__content', (docEl) => {
    console.log(`${testClass} started`)

    // Apply class immediately
    document.body.classList.add(testClass)

    // Protect the class with MutationObserver
    const bodyObserver = new MutationObserver(() => {
        if (!document.body.classList.contains(testClass)) {
            document.body.classList.add(testClass)
        }
    })

    bodyObserver.observe(document.body, {
        attributes: true,
        attributeFilter: ['class']
    })

    // Also use setInterval as backup (stops after 10 seconds)
    let counter = 0
    const interval = setInterval(() => {
        if (!document.body.classList.contains(testClass)) {
            document.body.classList.add(testClass)
        }
        counter++
        if (counter > 100) clearInterval(interval) // Stop after 10 seconds
    }, 100)
})
